CREATE TABLE Cablecard(id INTEGER NOT NULL primary key autoincrement,
Userid	INTEGER,
ChannelNumber	INTEGER NOT NULL,
ChannelName	VARCHAR(12) NOT NULL,
Receiving	VARCHAR(3),
LangField	VARCHAR(2),
Hd	INTEGER,
Dt	INTEGER,
ShortField	VARCHAR(12),
InNpl	INTEGER,
Net	VARCHAR(12),
CONSTRAINT FK_Cablecard_Userid FOREIGN KEY (Userid)    REFERENCES Account(Id));
